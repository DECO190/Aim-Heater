function dificultOff() {
    let mode = document.getElementById('hard-options')
    mode.style.display = 'none'

    let easy = document.getElementById('easy-options')
    easy.style.display = 'flex'

 
}

function dificultOn() {
    let mode = document.getElementById('hard-options')
    mode.style.display = 'flex'

    let easy = document.getElementById('easy-options')
    easy.style.display = 'none'



}